public class StringExercise {

    public static void main(String[] args) {

        String pinkFloyd = "We don't need no education \nWe don't need no thought control";
        System.out.println(pinkFloyd);

        String nestedQuotes = "Check \"this\" out!\", \"s inside of \"s!";
        System.out.println(nestedQuotes);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String last = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(last);

    }
}
